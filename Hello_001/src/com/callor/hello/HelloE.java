package com.callor.hello;

/*
 * main() method 를 선언하고
 * 정수형 숫자 30과 40을 변수에 저장한 후
 * 4칙연산 결과를 출력하시오
 */
public class HelloE {
	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println((float)num1 / num2);
	}

}
